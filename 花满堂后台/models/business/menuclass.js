var menuclass = function (schema) {
    var menuclass = schema.define('menuclass', {
        menuclassname: {type: String, length: 200},
        ishide: {type: Number, length: 11},//是否显示 0：不显示 1：显示
        menuclasstype: {type: Number, length: 11},// 1堂食 2外卖 3杂食
//        address: {type: String, length: 255},//地址
//        mobile: {type: String, length: 255},//联系电话
        createtime: {type: String, length: 200}//创建时间
    });
    return menuclass;
};

module.exports = menuclass;