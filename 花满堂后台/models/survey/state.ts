var state = function(){
    var state = state || {};

    state.STATE = {};

    Object.defineProperty(state.STATE, 'NONE', {
        value:0,
        writable: false
    });

    Object.defineProperty(state.STATE, 'REPORTING', {
        value:1,
        writable: false
    });

    Object.defineProperty(state.STATE, 'SURVEY', {
        value:1,
        writable: false
    });

    Object.defineProperty(state.STATE, 'SCHEDULEING', {
        value:1,
        writable: false
    });

    Object.defineProperty(state.STATE, 'SCHEDULED', {
        value:2,
        writable: false
    });

    Object.defineProperty(state.STATE, 'STOPSCHEDULEING', {
        value:3,
        writable: false
    });

    Object.defineProperty(state.STATE, 'RESCHEDULEING', {
        value:5,
        writable: false
    });

    Object.defineProperty(state.STATE, 'AUDITING', {
        value:1,
        writable: false
    });

    Object.defineProperty(state.STATE, 'AUDITED', {
        value:2,
        writable: false
    });

    Object.defineProperty(state.STATE, 'STOPAUDITING', {
        value:3,
        writable: false
    });

    return state;
};

export = state;